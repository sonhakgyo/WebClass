public class Hello { 
  public static void main(String[] args) {
    /* 외국 친구에게 인사를 해보겠다. */
    System.out.println("헬로 파이썬");
    /* 깃 저장소를 깔끔하게 사용해보겠습니다. */
    /* VSC와 이클립스를 이용해서 공부합니다. */

    /* 간단한 코드 쓸 공간입니다. */
    int a = 100;
    int result = 0;

    result = a >> 1;
    result = result + 1;

    result = a >> 2;
    result = result + 1;

    System.out.println(result);
  }
}